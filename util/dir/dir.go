package dir

import (
	"fmt"
	"os"
)

// Exists Returns true if the file called name exists; otherwise returns false.
func Exists(path string) bool {
	_, err := os.Stat(path)
	if err != nil {
		if os.IsExist(err) {
			return true
		}
		if os.IsNotExist(err) {
			return false
		}
		fmt.Println(err)
		return false
	}
	return true
}
